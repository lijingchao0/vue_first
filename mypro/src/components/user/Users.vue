<template>
<div>
    <!--面包屑导航-->
    <el-breadcrumb separator-class="el-icon-arrow-right">
        <el-breadcrumb-item :to="{ path: '/home' }">首页</el-breadcrumb-item>
        <el-breadcrumb-item>用户管理</el-breadcrumb-item>
        <el-breadcrumb-item>用户列表</el-breadcrumb-item>
    </el-breadcrumb>
    <!--卡片视图-->
    <el-card class="box-card">
        <!--用户列表区-->
        <el-row :gutter='20'>
            <el-col :span="7">
                <el-input placeholder="请输入内容" v-model="queryInfo.query" clearable @clear='getUserList'>
                    <el-button slot="append" icon="el-icon-search" @click="getUserList"></el-button>
                </el-input>
            </el-col>
            <el-col :span="4">
                <el-button type="primary" @click="dialogVisible = true">添加用户</el-button>
            </el-col>
        </el-row>
        <el-table :data='userlist' border stripe>
            <el-table-column label="#" type='index'></el-table-column>
            <el-table-column label="姓名" prop='username'></el-table-column>
            <el-table-column label="邮箱" prop='email'></el-table-column>
            <el-table-column label="电话" prop='mobile'></el-table-column>
            <el-table-column label="角色" prop='role_name'></el-table-column>
            <el-table-column label="状态" prop='mg_state'>
                <template slot-scope="scope">
                    <el-switch v-model="scope.row.mg_state" @change='userStateChanged(scope.row)'>
                    </el-switch>
                </template>

            </el-table-column>
            <el-table-column label="操作" width='180'>
                <template slot-scope="scope">
                    <el-button type="primary" icon="el-icon-edit" size='mini' @click="showEdit(scope.row.id)"></el-button>
                    <el-button type="danger" icon="el-icon-delete" size='mini' @click="removeUser(scope.row.id)"></el-button>
                    <el-tooltip class="item" effect="dark" content="分配角色" placement="top" :enterable='false'>
                        <el-button type="warning" icon="el-icon-setting" size='mini' @click="setRole(scope.row)"></el-button>
                    </el-tooltip>
                </template>

            </el-table-column>
        </el-table>
        <el-pagination @size-change="handleSizeChange" @current-change="handleCurrentChange" :current-page="queryInfo.pagenum" :page-sizes="[1, 2, 5, 40]" :page-size="queryInfo.pagesize" layout="total, sizes, prev, pager, next, jumper" :total="total">
        </el-pagination>
        <!-- 添加用户对话框 -->
        <el-dialog title="添加用户" :visible.sync="dialogVisible" width="50%" @close="dialogClose">
            <el-form :model="addForm" :rules="addFormRules" ref="addFormRef" label-width='70px'>
                <el-form-item label="用户名" prop="username">
                    <el-input v-model="addForm.username"></el-input>
                </el-form-item>
                <el-form-item label="密码" prop="password">
                    <el-input v-model="addForm.password"></el-input>
                </el-form-item>
                <el-form-item label="邮箱" prop="email">
                    <el-input v-model="addForm.email"></el-input>
                </el-form-item>
                <el-form-item label="电话" prop="mobile">
                    <el-input v-model="addForm.mobile"></el-input>
                </el-form-item>
            </el-form>
            <span slot="footer" class="dialog-footer">
                <el-button @click="dialogVisible = false">取 消</el-button>
                <el-button type="primary" @click="addUser">确 定</el-button>
            </span>
        </el-dialog>
        <!-- 修改用户对话框 -->
        <el-dialog title="修改用户" :visible.sync="editdialogVisible" width="30%" @close='dialogClose'>
            <el-form :model="editForm" :rules="addFormRules" ref="addFormRef" label-width='70px'>
                <el-form-item label="用户名">
                    <el-input v-model="editForm.username" :disabled="true"></el-input>
                </el-form-item>
                <el-form-item label="邮箱" prop='email'>
                    <el-input v-model="editForm.email"></el-input>
                </el-form-item>
                <el-form-item label="手机号" prop='mobile'>
                    <el-input v-model="editForm.mobile"></el-input>
                </el-form-item>
            </el-form>
            <span slot="footer" class="dialog-footer">
                <el-button @click="editdialogVisible = false">取 消</el-button>
                <el-button type="primary" @click="editUser">确 定</el-button>
            </span>
        </el-dialog>
        <!-- 分配角色对话框 -->
        <el-dialog title="提示" :visible.sync="setRoleDialogVisible" width="50%" @close='setDialogClose'>
            <div>
                <p>当前用户：{{userInfo.username}}</p>
                <p>当前角色：{{userInfo.role_name}}</p>
                <p>分配新角色：
                    <el-select v-model="selectId" placeholder="请选择">
                        <el-option v-for="item in rolesList" :key="item.id" :label="item.roleName" :value="item.id">
                        </el-option>
                    </el-select>
                </p>
            </div>
            <span slot="footer" class="dialog-footer">
                <el-button @click="setRoleDialogVisible = false">取 消</el-button>
                <el-button type="primary" @click="saveRole()">确 定</el-button>
            </span>
        </el-dialog>
    </el-card>
</div>
</template>

<script>
export default {
    data() {
        var checkEmail = (rule, value, cb) => {
            const regEmail = /^([a-zA-z0-9_-])+@([a-zA-z0-9_-])+(\.[a-zA-z0-9_-])+/
            if (regEmail.test(value)) {
                return cb()
            }
            cb(new Error('请输入合法的邮箱'))
        }
        var checkMobile = (rule, value, cb) => {
            const regMobile = /^(0|86|17951)?(13[0-9]|15[0123456789]|17[678]|18[0-9]|14[57])[0-9]{8}$/
            if (regMobile.test(value)) {
                return cb()
            }
            cb(new Error('请输入合法的手机号'))
        }
        // 获取用户列表的参数对象
        return {
            editdialogVisible: false,
            queryInfo: {
                query: '',
                pagenum: 1,
                pagesize: 2
            },
            userlist: [],
            total: 0,
            dialogVisible: false,
            addForm: {
                username: '',
                password: '',
                email: '',
                mobile: ''
            },
            addFormRules: {
                username: [{
                        required: true,
                        message: '请输入用户名',
                        trigger: 'blur'
                    },
                    {
                        min: 3,
                        max: 10,
                        message: '长度在3到10个字符之间',
                        trigger: 'blur'
                    }
                ],
                password: [{
                        required: true,
                        message: '请输入密码',
                        trigger: 'blur'
                    },
                    {
                        min: 6,
                        max: 15,
                        message: '长度在6到15个字符之间',
                        trigger: 'blur'
                    }
                ],
                email: [{
                        required: true,
                        message: '请输入用户名',
                        trigger: 'blur'
                    },
                    {
                        validator: checkEmail,
                        trigger: 'blur'
                    }
                ],
                mobile: [{
                        required: true,
                        message: '请输入用户名',
                        trigger: 'blur'
                    },
                    {
                        validator: checkMobile,
                        trigger: 'blur'
                    }
                ],
            },
            editForm: {

            },
            setRoleDialogVisible: false,
            userInfo: {},
            rolesList: [],
            selectId: ''
        }

    },
    created() {
        this.getUserList()
    },
    methods: {
        async getUserList() {
            const {
                data: res
            } = await this.$http.get('users', {
                params: this.queryInfo
            })
            if (res.meta.status !== 200) {
                return this.$message.error('获取用户列表失败')
            }
            this.userlist = res.data.users
            this.total = res.data.total

            console.log(res)
        },
        // 监听pagesize改变的事件
        handleSizeChange(newSize) {
            this.queryInfo.pagesize = newSize
            this.getUserList()
        },
        // 监听页码值改变的事件
        handleCurrentChange(newPage) {
            this.queryInfo.pagenum = newPage
            this.getUserList()
        },
        //监听switch开关转台的改变
        async userStateChanged(userinfo) {
            const {
                data: res
            } = await this.$http.put(`users/${userinfo.id}/state/${userinfo.mg_state}`)
            if (res.meta.status !== 200) {
                userinfo.mg_state = !userinfo.mg_state
                return this.$message.error('更新用户状态失败')
            }
            this.$message.success('更新用户状态成功')
        },
        //监听添加用户对话框关闭事件
        dialogClose() {
            this.$refs.addFormRef.resetFields();
        },
        // 点击按钮添加用户
        addUser() {
            this.$refs.addFormRef.validate(async vaild => {
                if (!vaild) return
                const {
                    data: res
                } = await this.$http.post('users', this.addForm)
                if (res.meta.status !== 201) {
                    return this.$message.error('添加用户失败')
                }
                this.dialogVisible = false
                this.$message.success('添加用户成功')
                this.getUserList()
            })
        },
        async showEdit(id) {
            this.editdialogVisible = true
            const {
                data: res
            } = await this.$http.get('users/' + id)
            if (res.meta.status !== 200) {
                return this.$message.error('获取用户信息失败')
            }
            this.editForm = res.data
            console.log(this.editForm)
        },
        editUser() {

            this.$refs.addFormRef.validate(async vaild => {
                if (!vaild) return
                const {
                    data: res
                } = await this.$http.put('users/' + this.editForm.id, {
                    email: this.editForm.email,
                    mobile: this.editForm.mobile
                })
                if (res.meta.status !== 200) {
                    return this.$message.error('更新用户信息失败')
                }
                this.editdialogVisible = false
                this.getUserList()
                this.$message.success('更新用户信息成功')
            })

        },
        async removeUser(id) {
            const confirmresult = await this.$confirm('此操作将永久删除用户, 是否继续?', '提示', {
                confirmButtonText: '确定',
                cancelButtonText: '取消',
                type: 'warning'
            }).catch(err => err)
            if (confirmresult !== 'confirm') {
                return this.$message.error('已取消删除')
            }
            const {
                data: res
            } = await this.$http.delete('users/' + id)
            if (res.meta.status !== 200) {
                return this.$message.error('删除用户失败')
            }
            this.$message.success('删除用户成功')
            this.getUserList()
        },
        // 展示分配角色的对话框
        async setRole(userInfo) {
            this.setRoleDialogVisible = true
            this.userInfo = userInfo
            const {
                data: res
            } = await this.$http.get('roles')
            if (res.meta.status !== 200) {
                return this.$message.error('获取角色列表失败')
            }
            this.rolesList = res.data
        },
        // 分配角色
        async saveRole(){
            if(!this.selectId){
                return this.$message.error('请选择要分配的角色')
            }
            const { data: res } = await this.$http.put(`users/${this.userInfo.id}/role`, {rid:this.selectId})
            if(res.meta.status !== 200) {
                return this.$message.error('更新角色失败')
            }
            this.$message.success('更新角色成功')
            this.getUserList()
            this.setRoleDialogVisible = false
        },
        setDialogClose() {
            this.selectId = ''
            this.userInfo = []
        }
    }
}
</script>

